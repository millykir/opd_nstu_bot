    def answer_by_rag(self, question: str) -> str:
        qvec = self._encode(question)
        qvec = np.array([qvec], dtype=np.float32)
        
        # –®–∞–≥ 1: –ò—â–µ–º –û–î–ù–û —Å–∞–º–æ–µ –ª—É—á—à–µ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –¥–ª—è –ø—Ä—è–º–æ–≥–æ –æ—Ç–≤–µ—Ç–∞
        distances, indices = self.index.search(qvec, 1)
        
        best_match_idx = indices[0][0]
        similarity = 0.0 # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –Ω–∞ —Å–ª—É—á–∞–π, –µ—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ
        if best_match_idx != -1:
            best_match_score = distances[0][0]
            # –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º L2 —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ FAISS –≤ –∏–Ω—Ç—É–∏—Ç–∏–≤–Ω–æ–µ "—Å—Ö–æ–¥—Å—Ç–≤–æ" –æ—Ç 0 –¥–æ 1
            similarity = float(best_match_score)

            # –®–∞–≥ 2: –ü—Ä–æ–≤–µ—Ä—è–µ–º, –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ –ª–∏ —Å—Ö–æ–¥—Å—Ç–≤–æ –¥–ª—è –ü–†–Ø–ú–û–ì–û –æ—Ç–≤–µ—Ç–∞
            if similarity >= DIRECT_ANSWER_THRESHOLD:
                print(f"...–Ω–∞–π–¥–µ–Ω–æ –ø—Ä—è–º–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ —Å —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç—å—é {similarity:.2f} >= {DIRECT_ANSWER_THRESHOLD}. –û—Ç–¥–∞—é —Ç–æ—á–Ω—ã–π –æ—Ç–≤–µ—Ç...")
                direct_answer_data = self.data_map[best_match_idx]
                return direct_answer_data.get('answer', "–ù–∞–π–¥–µ–Ω –æ—Ç–≤–µ—Ç, –Ω–æ –æ–Ω –ø—É—Å—Ç.")

        # –®–∞–≥ 3: –ï—Å–ª–∏ –ø—Ä—è–º–æ–≥–æ –æ—Ç–≤–µ—Ç–∞ –Ω–µ—Ç, –∏—Å–ø–æ–ª—å–∑—É–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π RAG —Å –≥–µ–Ω–µ—Ä–∞—Ü–∏–µ–π
        print(f"...–ø—Ä—è–º–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ –∏–ª–∏ —É–≤–µ—Ä–µ–Ω–Ω–æ—Å—Ç—å –Ω–∏–∑–∫–∞—è ({similarity:.2f} < {DIRECT_ANSWER_THRESHOLD}). –ü–µ—Ä–µ—Ö–æ–∂—É –≤ —Ä–µ–∂–∏–º –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ (RAG)...")
        distances, indices = self.index.search(qvec, TOP_K)
        found_vectors = []
        
        # –°–æ–±–∏—Ä–∞–µ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç —Ç–æ–ª—å–∫–æ –∏–∑ –¥–æ—Å—Ç–∞—Ç–æ—á–Ω–æ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã—Ö –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤
        for dist, idx in zip(distances[0], indices[0]):
            if idx != -1:
                doc_similarity = float(dist)
                if doc_similarity >= CONFIDENCE_THRESHOLD:
                    doc = dict(self.data_map[idx])
                    doc['_similarity'] = doc_similarity
                    found_vectors.append(doc)

        if not found_vectors:
            return "–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, —è –Ω–µ —Å–º–æ–≥ –Ω–∞–π—Ç–∏ —Ç–æ—á–Ω—ã–π –æ—Ç–≤–µ—Ç –Ω–∞ –≤–∞—à –≤–æ–ø—Ä–æ—Å –≤ –±–∞–∑–µ –∑–Ω–∞–Ω–∏–π. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–µ—Ä–µ—Ñ–æ—Ä–º—É–ª–∏—Ä–æ–≤–∞—Ç—å –µ–≥–æ –∏–ª–∏ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ –ü–ª–æ—Ç–Ω–∏–∫–æ–≤–æ–π –ù–∞—Ç–∞–ª—å–µ –í–ª–∞–¥–∏–º–∏—Ä–æ–≤–Ω–µ —á–µ—Ä–µ–∑ Dispace."
        
        # –£–±–∏—Ä–∞–µ–º –¥—É–±–ª–∏–∫–∞—Ç—ã –ø–æ ID –æ—Ä–∏–≥–∏–Ω–∞–ª—å–Ω–æ–≥–æ –≤–æ–ø—Ä–æ—Å–∞, —á—Ç–æ–±—ã –∫–æ–Ω—Ç–µ–∫—Å—Ç –±—ã–ª —á–∏—â–µ.
        unique_docs = list({doc.get('original_id'): doc for doc in found_vectors}.values())
        
        # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∫–ª—é—á 'question_variant', –∫–æ—Ç–æ—Ä—ã–π –µ—Å—Ç—å –≤ –≤–∞—à–µ–π –±–∞–∑–µ
        context_parts = [f"–í–æ–ø—Ä–æ—Å –∏–∑ –±–∞–∑—ã: {doc.get('question_variant', '–í–æ–ø—Ä–æ—Å –Ω–µ –Ω–∞–π–¥–µ–Ω')}\n–û—Ç–≤–µ—Ç –∏–∑ –±–∞–∑—ã: {doc.get('answer', '')}" for doc in unique_docs]
        context = '\n\n---\n\n'.join(context_parts)
        
        prompt = PROMPT_TEMPLATE.format(context=context, question=question)
        return call_ollama_api(prompt, temperature=0.15)
