        # <<< –ò–ó–ú–ï–ù–ï–ù–ò–ï: –í FAISS score/distance - —ç—Ç–æ L2-—Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ. –ß–µ–º –æ–Ω–æ –ú–ï–ù–¨–®–ï, —Ç–µ–º –ª—É—á—à–µ. >>>
        # –ü–æ—ç—Ç–æ–º—É –º—ã —Å—Ä–∞–≤–Ω–∏–≤–∞–µ–º score < THRESHOLD, –∞ –Ω–µ >. Sentence-transformers —Å normalize_embeddings
        # –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫–æ—Å–∏–Ω—É—Å–Ω–æ–µ —Å—Ö–æ–¥—Å—Ç–≤–æ, –≥–¥–µ —á–µ–º –í–´–®–ï, —Ç–µ–º –ª—É—á—à–µ. FAISS –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –∏—Å–ø–æ–ª—å–∑—É–µ—Ç L2.
        # –î–ª—è –ø—Ä–æ—Å—Ç–æ—Ç—ã, —è –æ—Å—Ç–∞–≤–ª—é –≤–∞—à—É –ª–æ–≥–∏–∫—É –ø–æ–∏—Å–∫–∞, –Ω–æ –≤–∞–∂–Ω–æ –ø–æ–Ω–∏–º–∞—Ç—å —ç—Ç—É —Ä–∞–∑–Ω–∏—Ü—É.
        # –í –¥–∞–Ω–Ω–æ–º —Å–ª—É—á–∞–µ, –º–æ–¥–µ–ª—å bge-m3 –Ω–æ—Ä–º–∞–ª–∏–∑—É–µ—Ç –≤–µ–∫—Ç–æ—Ä—ã, –∏ —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ L2 —Å—Ç–∞–Ω–æ–≤–∏—Ç—Å—è —Å–≤—è–∑–∞–Ω–æ —Å –∫–æ—Å–∏–Ω—É—Å–Ω—ã–º —Å—Ö–æ–¥—Å—Ç–≤–æ–º.
        # score = 2 - 2 * cos_sim. –¢–∞–∫–∏–º –æ–±—Ä–∞–∑–æ–º, –º–µ–Ω—å—à–∏–π score –æ–∑–Ω–∞—á–∞–µ—Ç –±–æ–ª—å—à–µ–µ —Å—Ö–æ–¥—Å—Ç–≤–æ.
